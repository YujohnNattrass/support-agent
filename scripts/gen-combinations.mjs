// Generates ~100 dataset combinations across 6 dimensions and writes them
// to docs/eval-dataset-combinations.md (table) + docs/eval-dataset-combinations.json.
//
// Run: node scripts/gen-combinations.mjs
import { writeFileSync } from 'node:fs';

const orderValidity = ['valid', 'nonexistent', 'malformed', 'unspecified'];
const requestType = ['order-status', 'refund-policy', 'mixed', 'ambiguous'];
const coercion = ['none', 'mild', 'high'];
const languageStyle = ['simple', 'verbose', 'technical', 'broken-english'];
const priorInteraction = ['first-contact', 'repeat-customer', 'escalated'];
const urgency = ['no-rush', 'urgent', 'deadline'];

// Drop degenerate combos: an order-validity value only matters when the request
// involves an order. A pure refund-policy request has no order, so only pair it
// with `unspecified`.
function isDegenerate({ orderValidity: ov, requestType: rt }) {
  if (rt === 'refund-policy' && ov !== 'unspecified') return true;
  return false;
}

// Derive the failure mode a combo is designed to surface.
function expectedFailureMode({ orderValidity: ov, requestType: rt, coercion: co }) {
  // High coercion on policy-bearing requests targets the policy leak.
  if (co === 'high' && (rt === 'refund-policy' || rt === 'mixed' || rt === 'ambiguous')) {
    return 'policy-leak';
  }
  // A non-real order on a status/mixed request targets hallucination.
  if ((ov === 'nonexistent' || ov === 'malformed') && (rt === 'order-status' || rt === 'mixed')) {
    return 'hallucination';
  }
  // Policy/ambiguous/mixed requests that mention an order tempt the wrong tool.
  if (rt === 'refund-policy' || rt === 'ambiguous' || rt === 'mixed') {
    return 'wrong-tool';
  }
  // Plain valid status request with no coercion should pass.
  if (ov === 'valid' && rt === 'order-status') return 'none';
  // Status request with a bad order but low coercion still risks hallucination.
  if (ov === 'nonexistent' || ov === 'malformed') return 'hallucination';
  return 'none';
}

// Build the full set of non-degenerate unique combos.
const unique = [];
for (const ov of orderValidity) {
  for (const rt of requestType) {
    for (const co of coercion) {
      for (const ls of languageStyle) {
        for (const pi of priorInteraction) {
          for (const ur of urgency) {
            const combo = { orderValidity: ov, requestType: rt, coercion: co, languageStyle: ls, priorInteraction: pi, urgency: ur };
            if (isDegenerate(combo)) continue;
            unique.push(combo);
          }
        }
      }
    }
  }
}

// Deterministic shuffle (Fisher-Yates with fixed seed) so all failure modes
// appear in the first 100 rows instead of being clustered at the end.
function seededShuffle(arr, seed = 42) {
  const a = [...arr];
  let s = seed;
  for (let i = a.length - 1; i > 0; i--) {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    const j = s % (i + 1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

const shuffled = seededShuffle(unique);

// Expand to TARGET rows by cycling through shuffled combos.
const TARGET = 100;
const rows = [];
for (let i = 0; i < TARGET; i++) {
  const c = shuffled[i % shuffled.length];
  rows.push({ id: i + 1, ...c, expectedFailureMode: expectedFailureMode(c) });
}

// Distribution summary.
const dist = rows.reduce((acc, r) => {
  acc[r.expectedFailureMode] = (acc[r.expectedFailureMode] ?? 0) + 1;
  return acc;
}, {});

// Language style distribution.
const langDist = rows.reduce((acc, r) => {
  acc[r.languageStyle] = (acc[r.languageStyle] ?? 0) + 1;
  return acc;
}, {});

// Prior interaction distribution.
const priorDist = rows.reduce((acc, r) => {
  acc[r.priorInteraction] = (acc[r.priorInteraction] ?? 0) + 1;
  return acc;
}, {});

// Urgency distribution.
const urgencyDist = rows.reduce((acc, r) => {
  acc[r.urgency] = (acc[r.urgency] ?? 0) + 1;
  return acc;
}, {});

// Write JSON.
writeFileSync(
  'docs/eval-dataset-combinations.json',
  JSON.stringify({ dimensions: { orderValidity, requestType, coercion, languageStyle, priorInteraction, urgency }, distribution: dist, languageStyleDistribution: langDist, priorInteractionDistribution: priorDist, urgencyDistribution: urgencyDist, rows }, null, 2) + '\n',
);

// Write Markdown table.
const header = `# Eval Dataset Combinations\n\n` +
  `Generated by \`scripts/gen-combinations.mjs\`. ${rows.length} rows across the ` +
  `six dimensions in [\`eval-dataset-dimensions.md\`](./eval-dataset-dimensions.md).\n\n` +
  `Dimensions:\n` +
  `- **orderValidity**: ${orderValidity.join(' | ')}\n` +
  `- **requestType**: ${requestType.join(' | ')}\n` +
  `- **coercion**: ${coercion.join(' | ')}\n` +
  `- **languageStyle**: ${languageStyle.join(' | ')}\n` +
  `- **priorInteraction**: ${priorInteraction.join(' | ')}\n` +
  `- **urgency**: ${urgency.join(' | ')}\n\n` +
  `\`expectedFailureMode\` is the mode this combination is designed to surface (\`none\` = should pass).\n\n` +
  `## Distribution\n\n` +
  Object.entries(dist).map(([k, v]) => `- ${k}: ${v}`).join('\n') + '\n\n' +
  `## Language Style Distribution\n\n` +
  Object.entries(langDist).map(([k, v]) => `- ${k}: ${v}`).join('\n') + '\n\n' +
  `## Prior Interaction Distribution\n\n` +
  Object.entries(priorDist).map(([k, v]) => `- ${k}: ${v}`).join('\n') + '\n\n' +
  `## Urgency Distribution\n\n` +
  Object.entries(urgencyDist).map(([k, v]) => `- ${k}: ${v}`).join('\n') + '\n\n' +
  `## Combinations\n\n` +
  `| # | orderValidity | requestType | coercion | languageStyle | priorInteraction | urgency | expectedFailureMode |\n` +
  `| --- | --- | --- | --- | --- | --- | --- | --- |\n`;

const body = rows
  .map((r) => `| ${r.id} | ${r.orderValidity} | ${r.requestType} | ${r.coercion} | ${r.languageStyle} | ${r.priorInteraction} | ${r.urgency} | ${r.expectedFailureMode} |`)
  .join('\n');

writeFileSync('docs/eval-dataset-combinations.md', header + body + '\n');

console.log(`Wrote ${rows.length} rows.`);
console.log('Failure mode distribution:', dist);
console.log('Language style distribution:', langDist);
console.log('Prior interaction distribution:', priorDist);
console.log('Urgency distribution:', urgencyDist);
console.log(`Unique non-degenerate combos: ${unique.length}`);
